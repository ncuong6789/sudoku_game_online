export const getGlobalMode = (totalTicks) => {
    let elapsed = totalTicks / 6; // 6 ticks per second
    let current = 0;
    for (let m of MODE_TIMES) {
        if (elapsed < current + m.time) return m.mode;
        current += m.time;
    }
    return 'CHASE';
};

